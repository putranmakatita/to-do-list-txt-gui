    public void displayTask(String search) {
        DefaultTableModel model = (DefaultTableModel) toDoListTable.getModel();
        model.setRowCount(0); // Hapus semua baris lama

        ArrayList<Object[]> tasksArr = logic.getAllTasks(search); // Ambil array 2 dimensi
        Object[][] tasks = logic.sortingByTanggal(tasksArr);

        for (Object[] row : tasks) {
            // Tambahkan tombol delete sebagai objek String atau JButton placeholder
            Object[] rowWithDelete = Arrays.copyOf(row, row.length + 1);
            rowWithDelete[row.length] = "Delete"; // Kolom terakhir: tombol Delete
            model.addRow(rowWithDelete);
        }

        // Pasang renderer dan editor untuk kolom tombol Delete, passing the JFrame
        // instance
        ButtonRendererEditor buttonRendererEditor = new ButtonRendererEditor(toDoListTable, this);
        toDoListTable.getColumnModel().getColumn(model.getColumnCount() - 1)
                .setCellRenderer(buttonRendererEditor);
        toDoListTable.getColumnModel().getColumn(model.getColumnCount() - 1)
                .setCellEditor(buttonRendererEditor);
    }

